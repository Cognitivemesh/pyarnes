class TestDependencyEdgesAgainstRealOverview:
    """The parser must produce the same edges a human reader pulls
    off the actual 00-overview.md. This is the contract test."""

    def test_matches_hand_extracted_edges(self) -> None:
        overview = serve_specs.CONSOLIDATION_ROOT / "00-overview.md"
        assert overview.is_file(), "fixture for the contract test is missing"
        # Bust the lru_cache so the test reflects the file at this moment.
        serve_specs._extract_dependency_edges.cache_clear()
        assert serve_specs._extract_dependency_edges(str(overview)) == EXPECTED_OVERVIEW_EDGES

    def test_no_orphan_targets(self) -> None:
        """Every spec referenced as a dependency target must itself
        exist as a markdown file. Catches typos in the source table
        without re-running the parser's own logic."""
        edges = serve_specs._extract_dependency_edges(str(serve_specs.CONSOLIDATION_ROOT / "00-overview.md"))
        targets = {t for ts in edges.values() for t in ts}
        for spec_num in targets:
            matches = list(serve_specs.CONSOLIDATION_ROOT.glob(f"{spec_num}-*.md"))
            assert matches, f"target {spec_num!r} has no matching spec file"


