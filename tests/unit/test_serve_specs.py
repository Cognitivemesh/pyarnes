class TestSpecMetaComputation:
    """Reading-time and section count are deterministic functions of body
    HTML; assert against ground-truth markup independent of the parser."""

    def test_reading_time_uses_220_wpm_and_minimum_one(self) -> None:
        body = "<h1>x</h1><p>" + " ".join(["word"] * 22) + "</p>"
        meta = serve_specs._compute_spec_meta(serve_specs.LANDING_PAGE, body)
        assert meta.reading_minutes == 1

    def test_reading_time_rounds_up(self) -> None:
        body = "<h1>x</h1><p>" + " ".join(["word"] * 221) + "</p>"
        meta = serve_specs._compute_spec_meta(serve_specs.LANDING_PAGE, body)
        assert meta.reading_minutes == 2

    def test_section_count_matches_h2_only(self) -> None:
        body = "<h1>x</h1><h2>a</h2><p>p</p><h2 id='b'>b</h2><h3>c</h3>"
        meta = serve_specs._compute_spec_meta(serve_specs.LANDING_PAGE, body)
        assert meta.section_count == 2  # h3 doesn't count

    def test_inject_meta_skips_when_no_h1(self) -> None:
        body = "<p>no h1 here</p>"
        meta = serve_specs.SpecMeta(reading_minutes=3, section_count=2, last_edited_relative=None)
        out = serve_specs._inject_spec_meta(body, meta)
        assert out == body  # unchanged

    def test_inject_meta_appears_after_h1(self) -> None:
        body = "<h1>Title</h1><p>p</p>"
        meta = serve_specs.SpecMeta(reading_minutes=4, section_count=3, last_edited_relative="2 days ago")
        out = serve_specs._inject_spec_meta(body, meta)
        # The chip lives between the closing </h1> and the next paragraph.
        assert '<div class="spec-meta">' in out
        assert "4 min read" in out
        assert "3 sections" in out
        assert "last edited 2 days ago" in out
        assert out.index('class="spec-meta"') > out.index("</h1>")
        assert out.index('class="spec-meta"') < out.index("<p>")


