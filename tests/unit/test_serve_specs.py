class TestPageEntriesMatchSidebar:
    """The data-island page list must equal what the sidebar walker
    sees, otherwise the command palette and prev/next pager will
    disagree with the visible nav."""

    def test_includes_landing_overview_consolidation_diagrams(self) -> None:
        entries = serve_specs._build_page_entries()
        slugs_by_group = {group: [e.slug for e in entries if e.group == group] for group in {e.group for e in entries}}

        # Ground truth: walk the filesystem with a glob the parser
        # doesn't share. If both methods agree, the index is faithful.
        expected_consolidation = sorted(
            p.stem for p in serve_specs.CONSOLIDATION_ROOT.glob("*.md") if p.name != "README.md"
        )
        expected_diagrams = sorted(p.stem for p in serve_specs.DIAGRAMS_ROOT.glob("*.html"))

        assert slugs_by_group["Overview"] == ["readme"]
        assert slugs_by_group["Consolidation"] == expected_consolidation
        assert slugs_by_group["Diagrams"] == expected_diagrams

    def test_landing_title_matches_first_h1(self) -> None:
        entries = serve_specs._build_page_entries()
        landing = next(e for e in entries if e.slug == "readme")
        first_line = serve_specs.LANDING_PAGE.read_text(encoding="utf-8").splitlines()[0]
        assert first_line.startswith("# ")
        assert landing.title == first_line[2:].strip()


class TestDataIslandShape:
    """The JSON shape is the contract between Python and specs.js.
    Pin every key the JS reads — silent renames or missing keys break
    the palette / drawer / artifacts without an obvious failure."""

    @pytest.fixture
    def parsed(self) -> dict[str, object]:
        rendered = serve_specs._render_data_island(serve_specs.LANDING_PAGE)
        prefix = '<script id="__specs_index__" type="application/json">'
        suffix = "</script>"
        assert rendered.startswith(prefix) and rendered.endswith(suffix)
        body = rendered[len(prefix) : -len(suffix)]
        # Reverse the </ defang the renderer applies.
        return json.loads(body.replace("<\\/", "</"))

    def test_top_level_keys(self, parsed: dict[str, object]) -> None:
        # specs.js + Alpine / HTMX consumers expect every key listed here.
        # If new keys are added the test should grow alongside them, not be
        # loosened to a subset check.
        assert set(parsed) == {
            "pages",
            "current",
            "deps",
            "artifacts",
            "spec_headers",
            "topic_chips",
        }

    def test_pages_have_required_fields(self, parsed: dict[str, object]) -> None:
        pages = parsed["pages"]
        assert isinstance(pages, list) and pages
        for page in pages:
            assert set(page) == {"slug", "title", "group", "href"}

    def test_artifacts_keys_match_placeholder_contract(self, parsed: dict[str, object]) -> None:
        # specs.js looks up data via div[data-artifact="<key>"] — these
        # three keys are wired to the three markdown placeholder edits.
        assert set(parsed["artifacts"]) == {"error-tree", "lifecycle-fsm", "hook-wiring"}

    def test_script_tag_defangs_closing_script(self) -> None:
        # An emitted JSON containing the literal "</script" would break
        # the surrounding HTML. The renderer must escape it.
        rendered = serve_specs._render_data_island(serve_specs.LANDING_PAGE)
        # Strip the trailing legitimate </script> close tag, then ensure
        # no other "</script" appears inside the JSON body.
        body = rendered[: -len("</script>")]
        assert "</script" not in body


