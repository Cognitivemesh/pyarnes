class TestTopicChipsAggregation:
    """Topic chips are derived from every spec's Tags row."""

    def test_topic_chips_only_above_threshold(self) -> None:
        # Default filter drops tags appearing in fewer than 3 specs to keep
        # the chip strip compact. Counts must respect the threshold.
        chips = dict(serve_specs._all_topic_chips())
        assert chips, "expected at least one tag with >=3 occurrences"
        for tag, count in chips.items():
            assert isinstance(count, int) and count >= 3, (tag, count)

    def test_topic_chips_min_count_parameter(self) -> None:
        # Lowering the threshold surfaces tags that the default would hide.
        with_low = dict(serve_specs._all_topic_chips(min_count=1))
        with_default = dict(serve_specs._all_topic_chips())
        assert set(with_default).issubset(with_low)
        assert len(with_low) >= len(with_default)

    def test_topic_chips_use_canonical_tags(self) -> None:
        # Synonym folding: `hooks` / `in-process` collapse into `lifecycle`,
        # so the canonical form must be the one that surfaces (when above
        # threshold), and the aliases must not appear as separate chips.
        chips = dict(serve_specs._all_topic_chips(min_count=1))
        assert "hooks" not in chips
        assert "in-process" not in chips
        assert "guardrails" not in chips


