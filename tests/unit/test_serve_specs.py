"""Tests for the specs-viewer helpers.

Each test anchors on ground truth derived independently of the parser
(by hand-reading the markdown, walking the filesystem differently, or
checking the JSON shape against the JS contract). A test that re-derived
its expectation from the same regex it's supposed to guard would only
prove the author can copy a pattern.
"""

from __future__ import annotations

import importlib.util
import json
import sys
from pathlib import Path

import pytest

# scripts/serve_specs.py is a uv inline-script; load it directly so the
# tests don't depend on a `scripts` package on sys.path. The module must
# be registered in sys.modules before exec_module so dataclass() can
# resolve its enclosing module by name.
_SPEC_PATH = Path(__file__).resolve().parents[2] / "scripts" / "serve_specs.py"
_spec = importlib.util.spec_from_file_location("serve_specs", _SPEC_PATH)
assert _spec is not None and _spec.loader is not None
serve_specs = importlib.util.module_from_spec(_spec)
sys.modules["serve_specs"] = serve_specs
_spec.loader.exec_module(serve_specs)


